  <h3>2. So this works on my own website?</h3>
  <div>
  Yes, PlusPanda uses a small javascript file to dynamically and remotely
  load your entire testimonial environment.
  
  You just need 2 lines of code which are provided for you in the admin panel.
  Put the code wherever you want your testimonials to go and everything runs transparently on that page.
  </div>

  <h3>3. I already have a testimonial page, why change?</h3>
  <div>
